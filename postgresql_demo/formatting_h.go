type FromCharDateMode int

type KeyWord struct {
	name      string
	len       int
	id        int
	is_digit  bool
	date_mode FromCharDateMode
}

type NumKeyWord struct {
	name string
	len  int
	id   int
}

/* ----------
 * Number description struct
 * ----------
 */
type NUMDesc struct {
	pre           int /* (count) numbers before decimal */
	post          int /* (count) numbers after decimal  */
	lsign         int /* want locales sign		  */
	flag          int /* number parameters		  */
	pre_lsign_num int /* tmp value for lsign		  */
	multi         int /* multiplier for 'V'		  */
	zero_start    int /* position of first zero	  */
	zero_end      int /* position of last zero	  */
	need_locale   int /* needs it locale		  */
}

/* ----------
 * Format parser structs
 * ----------
 */
type KeySuffix struct {
	name string /* suffix string		*/
	len  int    /* suffix length		*/
	id   int    /* used in node->suffix */
	typ  int    /* prefix / postfix		*/
}

type FormatNode struct {
	typ    uint8    /* NODE_TYPE_XXX, see below */
	char   byte     /* if type is CHAR ??? */
	suffix uint8    /* keyword prefix/suffix code, if any */
	key    *KeyWord /* if type is ACTION */
}

type DCHCacheEntry struct {
	format [DCH_CACHE_SIZE + 1]FormatNode
	str    [DCH_CACHE_SIZE + 1]byte
	age    int
}

type NUMCacheEntry struct {
	format [NUM_CACHE_SIZE + 1]FormatNode
	str    [NUM_CACHE_SIZE + 1]byte
	age    int
	Num    NUMDesc
}

type TmFromChar struct {
	mode                                                                         FromCharDateMode
	hh, pm, mi, ss, sssss, d, dd, ddd, mm, ms, year, bc, ww, w, cc, j, us, yysz, /* is it YY or YYYY ? */
	clock int /* 12 or 24 hour clock? */
}

